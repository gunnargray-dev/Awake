def _detect_test_drops(records: list[_SessionData]) -> list[Anomaly]:
    """Detect sessions where cumulative test count decreased."""
    anomalies: list[Anomaly] = []
    prev_tests = 0
    for rec in records:
        if rec.tests > 0:
            if prev_tests > 0 and rec.tests < prev_tests:
                drop = prev_tests - rec.tests
                anomalies.append(Anomaly(
                    severity="CRITICAL",
                    session=rec.number,
                    kind="test_count_drop",
                    description=(
                        f"Test count dropped from {prev_tests} to {rec.tests} "
                        f"(lost {drop} tests). Tests should only increase across "
                        f"sessions unless a module was intentionally removed."
                    ),
                    metric_name="cumulative_tests",
                    metric_value=float(rec.tests),
                    expected_range=f">= {prev_tests}",
                    suggested_action=(
                        "Investigate whether tests were deleted intentionally. "
                        "Check git diff for removed test files."
                    ),
                ))
            prev_tests = rec.tests
    return anomalies


def _detect_complexity_spikes(records: list[_SessionData]) -> list[Anomaly]:
    """Detect sessions with disproportionately many lines vs tests added."""
    anomalies: list[Anomaly] = []

    # Compute per-session test deltas
    prev_tests = 0
    session_ratios: list[tuple[_SessionData, float]] = []
    for rec in records:
        tests_added = max(0, rec.tests - prev_tests) if rec.tests > 0 else 0
        if rec.lines_added > 0 and tests_added > 0:
            ratio = rec.lines_added / tests_added
            session_ratios.append((rec, ratio))
        elif rec.lines_added > 100 and tests_added == 0:
            # Lots of lines with zero tests is always suspicious
            anomalies.append(Anomaly(
                severity="WARNING",
                session=rec.number,
                kind="complexity_spike",
                description=(
                    f"Session {rec.number} added ~{rec.lines_added} lines "
                    f"but reported 0 new tests. This may indicate untested code "
                    f"was introduced."
                ),
                metric_name="lines_per_test",
                metric_value=float(rec.lines_added),
                expected_range="lines/tests ratio < 50",
                suggested_action=(
                    "Review the code added in this session for test coverage gaps."
                ),
            ))
        if rec.tests > 0:
            prev_tests = rec.tests

    if len(session_ratios) >= 3:
        ratios = [r for _, r in session_ratios]
        lo, hi = _iqr_bounds(ratios)
        for rec, ratio in session_ratios:
            if ratio > hi and ratio > 50:
                anomalies.append(Anomaly(
                    severity="WARNING",
                    session=rec.number,
                    kind="complexity_spike",
                    description=(
                        f"Session {rec.number} had a lines-to-tests ratio of "
                        f"{ratio:.1f} (added ~{rec.lines_added} lines for "
                        f"{int(rec.lines_added / ratio)} new tests). "
                        f"This is above the IQR upper bound of {hi:.1f}."
                    ),
                    metric_name="lines_per_test",
                    metric_value=round(ratio, 1),
                    expected_range=f"{lo:.1f} – {hi:.1f}",
                    suggested_action=(
                        "Check if the lines added are boilerplate, data, or "
                        "generated code that doesn't need proportional tests."
                    ),
                ))

    return anomalies


def _detect_velocity_changes(records: list[_SessionData]) -> list[Anomaly]:
    """Detect dramatic changes in time between sessions."""
    anomalies: list[Anomaly] = []

    if len(records) < 3:
        return anomalies

    # Compute gaps in days between consecutive sessions
    gaps: list[tuple[_SessionData, float]] = []
    for i in range(1, len(records)):
        try:
            d1 = datetime.strptime(records[i - 1].date, "%Y-%m-%d")
            d2 = datetime.strptime(records[i].date, "%Y-%m-%d")
            gap_days = (d2 - d1).days
            gaps.append((records[i], float(gap_days)))
        except ValueError:
            continue

    if len(gaps) < 3:
        return anomalies

    gap_values = [g for _, g in gaps]
    lo, hi = _iqr_bounds(gap_values)
    m = _mean(gap_values)
    sd = _stdev(gap_values)

    for rec, gap in gaps:
        z = _z_score(gap, m, sd)
        if gap > hi and abs(z) > 2.0 and gap > 3:
            anomalies.append(Anomaly(
                severity="INFO",
                session=rec.number,
                kind="velocity_gap",
                description=(
                    f"Session {rec.number} came {int(gap)} days after the "
                    f"previous session — significantly longer than the average "
                    f"gap of {m:.1f} days (z-score: {z:.1f})."
                ),
                metric_name="days_since_previous",
                metric_value=gap,
                expected_range=f"{max(0, lo):.1f} – {hi:.1f} days",
                suggested_action=(
                    "Consider whether external factors caused the gap. "
                    "Long gaps may indicate blocked dependencies or priority shifts."
                ),
            ))
        elif gap < lo and abs(z) > 2.0 and gap < 0:
            # Negative gap would mean date ordering issues
            anomalies.append(Anomaly(
                severity="WARNING",
                session=rec.number,
                kind="date_ordering_issue",
                description=(
                    f"Session {rec.number} appears to have a date "
                    f"before the previous session ({gap} days gap). "
                    f"This may indicate a logging error."
                ),
                metric_name="days_since_previous",
                metric_value=gap,
                expected_range=f">= 0 days",
                suggested_action="Check AWAKE_LOG.md dates for this session.",
            ))

    # Also detect burst: many sessions on same day
    from collections import Counter
    date_counts = Counter(r.date for r in records)
    for date, count in date_counts.items():
        if count >= 4:
            burst_sessions = [r.number for r in records if r.date == date]
            anomalies.append(Anomaly(
                severity="INFO",
                session=burst_sessions[0],
                kind="session_burst",
                description=(
                    f"{count} sessions ran on {date} "
                    f"(sessions {', '.join(str(s) for s in burst_sessions)}). "
                    f"This is a marathon build day — unusual even for an AI."
                ),
                metric_name="sessions_per_day",
                metric_value=float(count),
                expected_range="1 – 3 sessions per day",
                suggested_action=(
                    "Verify all sessions completed successfully. "
                    "Marathon days increase risk of accumulated errors."
                ),
            ))

    return anomalies


def _detect_missing_metrics(records: list[_SessionData]) -> list[Anomaly]:
    """Detect sessions with missing expected stats fields."""
    anomalies: list[Anomaly] = []

    for rec in records:
        missing: list[str] = []
        if not rec.has_prs_stat and rec.prs == 0:
            missing.append("PRs opened")
        if not rec.has_tests_stat:
            missing.append("Tests")
        if not rec.has_modules_stat:
            missing.append("Source modules")

        if len(missing) >= 2:
            anomalies.append(Anomaly(
                severity="WARNING",
                session=rec.number,
                kind="missing_metrics",
                description=(
                    f"Session {rec.number} is missing {len(missing)} expected "
                    f"stats fields: {', '.join(missing)}. Incomplete entries "
                    f"reduce the accuracy of trend analysis."
                ),
                metric_name="missing_fields",
                metric_value=float(len(missing)),
                expected_range="0 missing fields",
                suggested_action=(
                    "Update the AWAKE_LOG.md entry for this session with "
                    "the missing stats."
                ),
            ))
        elif len(missing) == 1:
            anomalies.append(Anomaly(
                severity="INFO",
                session=rec.number,
                kind="missing_metrics",
                description=(
                    f"Session {rec.number} is missing the '{missing[0]}' "
                    f"stat field."
                ),
                metric_name="missing_fields",
                metric_value=1.0,
                expected_range="0 missing fields",
                suggested_action=(
                    f"Add the {missing[0]} stat to this session's log entry."
                ),
            ))

    return anomalies


def _detect_pr_anomalies(records: list[_SessionData]) -> list[Anomaly]:
    """Detect unusually high or low PR counts."""
    anomalies: list[Anomaly] = []

    pr_counts = [float(r.prs) for r in records if r.prs > 0]
    if len(pr_counts) < 3:
        return anomalies

    lo, hi = _iqr_bounds(pr_counts)
    m = _mean(pr_counts)
    sd = _stdev(pr_counts)

    for rec in records:
        if rec.prs == 0:
            continue  # handled by missing_metrics
        z = _z_score(float(rec.prs), m, sd)
        if rec.prs > hi and abs(z) > 2.0:
            anomalies.append(Anomaly(
                severity="INFO",
                session=rec.number,
                kind="pr_count_high",
                description=(
                    f"Session {rec.number} opened {rec.prs} PRs — "
                    f"significantly above the average of {m:.1f} "
                    f"(z-score: {z:.1f}). This was an exceptionally "
                    f"productive session."
                ),
                metric_name="prs_opened",
                metric_value=float(rec.prs),
                expected_range=f"{max(0, lo):.1f} – {hi:.1f}",
                suggested_action=(
                    "No action needed — high PR count indicates a burst of "
                    "productivity. Verify all PRs passed CI."
                ),
            ))

    return anomalies


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def detect_anomalies(
    repo_path: Path,
    log_path: Optional[Path] = None,
) -> AnomalyReport:
    """Parse AWAKE_LOG.md and detect anomalies in session metrics.

    Args:
        repo_path: Root of the Awake repository.
        log_path:  Explicit path to the log file. If None, defaults to
                   ``repo_path / "AWAKE_LOG.md"``.

    Returns:
        AnomalyReport with all detected anomalies.
    """
    if log_path is None:
        log_path = repo_path / "AWAKE_LOG.md"

    log_text = ""
    if log_path.exists():
        log_text = log_path.read_text(encoding="utf-8")

    records = _parse_log(log_text)

    if not records:
        return AnomalyReport(
            sessions_analyzed=0,
            total_anomalies=0,
            critical_count=0,
            warning_count=0,
            info_count=0,
            anomalies=[],
        )

    # Run all detectors
    all_anomalies: list[Anomaly] = []
    all_anomalies.extend(_detect_test_drops(records))
    all_anomalies.extend(_detect_complexity_spikes(records))
    all_anomalies.extend(_detect_velocity_changes(records))
    all_anomalies.extend(_detect_missing_metrics(records))
    all_anomalies.extend(_detect_pr_anomalies(records))

    # Sort: CRITICAL first, then WARNING, then INFO; within severity by session
    severity_order = {"CRITICAL": 0, "WARNING": 1, "INFO": 2}
    all_anomalies.sort(key=lambda a: (severity_order.get(a.severity, 9), a.session))

    critical = sum(1 for a in all_anomalies if a.severity == "CRITICAL")
    warning = sum(1 for a in all_anomalies if a.severity == "WARNING")
    info = sum(1 for a in all_anomalies if a.severity == "INFO")

    return AnomalyReport(
        sessions_analyzed=len(records),
        total_anomalies=len(all_anomalies),
        critical_count=critical,
        warning_count=warning,
        info_count=info,
        anomalies=all_anomalies,
    )


# ---------------------------------------------------------------------------
# Entry point (standalone)
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    import sys
    repo = Path(sys.argv[1]) if len(sys.argv) > 1 else Path(".")
    report = detect_anomalies(repo)
    print(report.to_markdown())
