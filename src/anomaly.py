"""
anomaly.py — Anomaly detection engine for Awake session metrics.

Parses AWAKE_LOG.md and detects unusual patterns across sessions:
  - Sudden test count drops
  - Complexity spikes (high lines-added : tests-added ratio)
  - Session velocity changes (time between sessions)
  - Missing metrics in session entries
  - PR count anomalies (unusually high or low)

Uses IQR-based outlier detection — no ML dependencies, pure stdlib.

Public API
----------
detect_anomalies(repo_path, log_path=None) -> AnomalyReport
"""

from __future__ import annotations

import json
import math
import re
from dataclasses import dataclass, field, asdict
from datetime import datetime
from pathlib import Path
from typing import Optional


# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------


@dataclass
class Anomaly:
    """A single detected anomaly in session metrics."""

    severity: str          # "INFO", "WARNING", "CRITICAL"
    session: int           # session number where the anomaly was detected
    kind: str              # short machine-readable type tag
    description: str       # human-readable explanation
    metric_name: str       # which metric triggered the anomaly
    metric_value: float    # the anomalous value
    expected_range: str    # e.g. "12.0 – 48.0"
    suggested_action: str  # what to do about it

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass
class AnomalyReport:
    """Full anomaly detection report across all sessions."""

    sessions_analyzed: int
    total_anomalies: int
    critical_count: int
    warning_count: int
    info_count: int
    anomalies: list        # list[Anomaly]

    def to_dict(self) -> dict:
        return {
            "sessions_analyzed": self.sessions_analyzed,
            "total_anomalies": self.total_anomalies,
            "critical_count": self.critical_count,
            "warning_count": self.warning_count,
            "info_count": self.info_count,
            "anomalies": [a.to_dict() for a in self.anomalies],
        }

    def to_json(self) -> str:
        return json.dumps(self.to_dict(), indent=2)

    def to_markdown(self) -> str:
        lines: list[str] = []
        lines.append("# Awake: Anomaly Detection Report")
        lines.append("")
        lines.append(
            "> *An AI monitoring its own development metrics — "
            "because the system that builds itself should also watch itself.*"
        )
        lines.append("")
        lines.append("---")
        lines.append("")

        # Summary
        lines.append("## Summary")
        lines.append("")
        lines.append("| Metric | Value |")
        lines.append("|--------|-------|")
        lines.append(f"| Sessions analyzed | {self.sessions_analyzed} |")
        lines.append(f"| Total anomalies | {self.total_anomalies} |")
        lines.append(f"| Critical | {self.critical_count} |")
        lines.append(f"| Warning | {self.warning_count} |")
        lines.append(f"| Info | {self.info_count} |")
        lines.append("")

        if not self.anomalies:
            lines.append("*No anomalies detected. All metrics within expected ranges.*")
            lines.append("")
        else:
            # Group by severity
            for sev in ("CRITICAL", "WARNING", "INFO"):
                group = [a for a in self.anomalies if a.severity == sev]
                if not group:
                    continue
                icon = {"CRITICAL": "[!!]", "WARNING": "[!]", "INFO": "[i]"}[sev]
                lines.append(f"## {icon} {sev} ({len(group)})")
                lines.append("")
                for a in group:
                    lines.append(f"### Session {a.session}: {a.kind}")
                    lines.append("")
                    lines.append(a.description)
                    lines.append("")
                    lines.append(f"- **Metric:** {a.metric_name} = {a.metric_value}")
                    lines.append(f"- **Expected range:** {a.expected_range}")
                    lines.append(f"- **Action:** {a.suggested_action}")
                    lines.append("")

        lines.append("---")
        lines.append("")
        lines.append(
            "*Generated by `awake anomalies` — "
            "the AI that builds itself now monitors itself.*"
        )
        lines.append("")
        return "\n".join(lines)


# ---------------------------------------------------------------------------
# AWAKE_LOG.md parser (reuse patterns from insights.py)
# ---------------------------------------------------------------------------

_RE_SESSION_HEADER = re.compile(
    r"^##\s+Session\s+(\d+)\s*--\s*(.+?)\s*\((\d{4}-\d{2}-\d{2})\)",
    re.MULTILINE,
)
_RE_STAT_MODULES = re.compile(
    r"\|\s*Source modules\s*\|\s*([\d,]+)\s*\|", re.IGNORECASE
)
_RE_STAT_TESTS = re.compile(
    r"\|\s*Tests\s*\|\s*([\d,~]+\+?)\s*\|", re.IGNORECASE
)
_RE_STAT_PRS = re.compile(
    r"\|\s*PRs opened\s*\|\s*([\d,]+)\s*\|", re.IGNORECASE
)
_RE_STAT_LINES = re.compile(
    r"\|\s*Lines added\s*\|\s*~?([\d,]+)\s*\|", re.IGNORECASE
)
_RE_STAT_TESTS_PASSING = re.compile(
    r"\|\s*Tests\s*(?:\(local\))?\s*\|\s*([\d,]+)\s*passed", re.IGNORECASE
)
_RE_PR_LINE = re.compile(r"^\s*[-*]\s*PR\s*#(\d+)", re.MULTILINE)


@dataclass
class _SessionData:
    """Internal parsed record with raw metrics."""

    number: int
    date: str
    title: str
    prs: int
    modules: int          # cumulative source modules (0 if not reported)
    tests: int            # cumulative test count (0 if not reported)
    lines_added: int      # lines added this session (0 if not reported)
    has_modules_stat: bool
    has_tests_stat: bool
    has_prs_stat: bool
    has_lines_stat: bool


def _parse_int(text: str) -> int:
    cleaned = re.sub(r"[,~+]", "", text.strip())
    try:
        return int(cleaned)
    except ValueError:
        return 0


def _parse_log(log_text: str) -> list[_SessionData]:
    """Parse AWAKE_LOG.md into session data records."""
    session_matches = list(_RE_SESSION_HEADER.finditer(log_text))
    if not session_matches:
        return []

    records: list[_SessionData] = []
    for idx, match in enumerate(session_matches):
        start = match.start()
        end = (
            session_matches[idx + 1].start()
            if idx + 1 < len(session_matches)
            else len(log_text)
        )
        chunk = log_text[start:end]

        session_num = int(match.group(1))
        session_date = match.group(3).strip()
        session_title = match.group(2).strip()

        modules_match = _RE_STAT_MODULES.search(chunk)
        tests_match = _RE_STAT_TESTS.search(chunk)
        prs_stat_match = _RE_STAT_PRS.search(chunk)
        lines_match = _RE_STAT_LINES.search(chunk)
        tests_passing = _RE_STAT_TESTS_PASSING.search(chunk)

        cumulative_modules = _parse_int(modules_match.group(1)) if modules_match else 0
        cumulative_tests = _parse_int(tests_match.group(1)) if tests_match else 0
        if not cumulative_tests and tests_passing:
            cumulative_tests = _parse_int(tests_passing.group(1))
        lines_added = _parse_int(lines_match.group(1)) if lines_match else 0

        if prs_stat_match:
            pr_count = _parse_int(prs_stat_match.group(1))
        else:
            pr_count = len(_RE_PR_LINE.findall(chunk))

        records.append(_SessionData(
            number=session_num,
            date=session_date,
            title=session_title,
            prs=pr_count,
            modules=cumulative_modules,
            tests=cumulative_tests,
            lines_added=lines_added,
            has_modules_stat=modules_match is not None,
            has_tests_stat=(tests_match is not None or tests_passing is not None),
            has_prs_stat=prs_stat_match is not None,
            has_lines_stat=lines_match is not None,
        ))

    return sorted(records, key=lambda r: r.number)


# ---------------------------------------------------------------------------
# Statistical helpers
# ---------------------------------------------------------------------------


def _iqr_bounds(values: list[float], k: float = 1.5) -> tuple[float, float]:
    """Compute IQR-based lower/upper bounds for outlier detection.

    Returns (lower_bound, upper_bound).  Values outside these bounds
    are considered outliers.
    """
    if len(values) < 4:
        if not values:
            return (0.0, 0.0)
        return (min(values), max(values))

    s = sorted(values)
    n = len(s)
    q1 = s[n // 4]
    q3 = s[(3 * n) // 4]
    iqr = q3 - q1
    return (q1 - k * iqr, q3 + k * iqr)


def _mean(values: list[float]) -> float:
    if not values:
        return 0.0
    return sum(values) / len(values)


def _stdev(values: list[float]) -> float:
    if len(values) < 2:
        return 0.0
    m = _mean(values)
    return math.sqrt(sum((v - m) ** 2 for v in values) / (len(values) - 1))


def _z_score(value: float, mean: float, stdev: float) -> float:
    if stdev == 0:
        return 0.0
    return (value - mean) / stdev


